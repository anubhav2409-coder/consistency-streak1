import java.util.*;
public class percentageof4subject {
    public static void main(String[] args){

        //percentage of 4 subject

        double x = 55;
        double y = 56;
        double z = 57;
        double a = 58;

        //Each subject of 60 marks

        //USE CTRL+/ TO COMMENT OUT MORE LINES

        double sum = x + y + z + a;
        double p = (sum / 240) * 100;

        System.out.println(p);
    }
    
}
